/**
 * LOS Field Library
 * Comprehensive library of all possible loan origination system fields
 * Allows lenders to customize field display names while preserving original source
 */

export interface LOSField {
  /** Original field name/key (used in database and API) */
  sourceKey: string;
  /** Display name (customizable by lender) */
  displayName: string;
  /** Field category */
  category: 'basic' | 'borrower' | 'property' | 'financial' | 'underwriting' | 'closing' | 'servicing' | 'metadata';
  /** Data type */
  dataType: 'string' | 'number' | 'date' | 'boolean' | 'currency' | 'percentage';
  /** Whether field is required */
  required: boolean;
  /** Description/help text */
  description?: string;
  /** Common aliases/variations of this field name */
  aliases?: string[];
  /** Encompass ICE field ID (e.g., "Fields.15", "Fields.4118") */
  encompassFieldId?: string;
}

