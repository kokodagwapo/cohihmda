/**
 * Get field by source key
 */
export function getFieldBySourceKey(sourceKey: string): LOSField | undefined {
  return LOS_FIELD_LIBRARY.find(field => field.sourceKey === sourceKey);
}

/**
 * Find field by alias (case-insensitive)
 */
export function findFieldByAlias(alias: string): LOSField | undefined {
  const normalizedAlias = alias.toLowerCase().trim();
  return LOS_FIELD_LIBRARY.find(field => {
    if (field.sourceKey.toLowerCase() === normalizedAlias) return true;
    if (field.displayName.toLowerCase() === normalizedAlias) return true;
    return field.aliases?.some(a => a.toLowerCase() === normalizedAlias);
  });
}

/**
 * Get all fields by category
 */
export function getFieldsByCategory(category: LOSField['category']): LOSField[] {
  return LOS_FIELD_LIBRARY.filter(field => field.category === category);
}

/**
 * Field Mapping Configuration
 * Allows lenders to customize display names while preserving source keys
 */
export interface FieldMappingConfig {
  /** Source field key (from CSV/LOS) */
  sourceField: string;
  /** Target field key (in our system) */
  targetField: string;
  /** Custom display name (optional, uses library default if not provided) */
  customDisplayName?: string;
}

/**
 * Create field mapping from CSV headers to system fields
 */
export function createFieldMapping(
  csvHeaders: string[],
  customMappings?: Record<string, string>
): Record<string, string> {
  const mapping: Record<string, string> = {};

  for (const header of csvHeaders) {
    // Check custom mappings first
    if (customMappings && customMappings[header]) {
      mapping[header] = customMappings[header];
      continue;
    }

    // Try to find matching field in library
    const field = findFieldByAlias(header);
    if (field) {
      mapping[header] = field.sourceKey;
    } else {
      // If no match found, use header as-is (will be stored in raw_data)
      mapping[header] = header;
    }
  }

  return mapping;
}

/**
 * Get display name for a field (respects custom display names)
 */
export function getFieldDisplayName(
  sourceKey: string,
  customDisplayNames?: Record<string, string>
): string {
  if (customDisplayNames && customDisplayNames[sourceKey]) {
    return customDisplayNames[sourceKey];
  }

  const field = getFieldBySourceKey(sourceKey);
  return field?.displayName || sourceKey;
}
